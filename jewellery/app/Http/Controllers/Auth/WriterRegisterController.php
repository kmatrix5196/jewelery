    protected function createWriter(Request $request)
    {
        $this->validatorWriter($request->all())->validate();
        $writer = Writer::create([
            'name' => $request['name'],
            'email' => $request['email'],
            'password' => Hash::make($request['password']),
        ]);
        return redirect()->intended('writer/login');
    }
}
